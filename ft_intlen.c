#include "ft_printf.h"

int	ft_intlen(long int i)
{
	int	len;
	int	neg;

	neg = 0;
	len = 0;
	if (i < 0)
	{
		i = i * -1;
		neg = 1;
	}
	if (i == 0)
		len = 1;
	while (i > 0)
	{
		i = i / 10;
		len++;
	}
	return (len + neg);
}
